package com.demo.poi.xlsx;

/**
 * 单元格样式
 */
public enum XlsxCellStyleType {
    /**
     * 普通文本样式
     */
    NORMAL,
    /**
     * 表头样式
     */
    TITLE,
    /**
     * 只读样式
     */
    READONLY,
    /**
     * 存在错误信息样式
     */
    ERROR,
    /**
     * 红色字体样式
     */
    RED_FONT,
    /**
     * 左上对齐样式
     */
    LEFT_TOP;
}
